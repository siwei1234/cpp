int addcopy(string& tmp, string& s)
{
	int ret = 0;
	int pos = -1;
	while ((pos = s.find(tmp, pos + 1)) >= 0)
	{
		ret++;
	}
	return ret;
}

int main()
{
	string s = "s121210";
	string ret = " ";
	int max = 0;
	for (int i = 0; i < s.size() - 1; i++)
	{
		for (int j = 2; j <= s.size(); j++)
		{
			if (j - i <= 1)
				continue;
			string tmp = s.substr(i, j - i);
			int t = addcopy(tmp, s);
			if (t > 1 && t >= max)
			{
				if (t == max)
				{
					if (tmp.size() > ret.size())
						ret = tmp;
				}
				else
				{
					ret = tmp;
					max = t;
				}
			}
		}
	}
	cout << ret << endl;
	return 0;
}